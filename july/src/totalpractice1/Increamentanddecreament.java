package totalpractice1;

public class Increamentanddecreament {public static void main(String[] args) {
	int a=1;
	
	System.out.println(a);
	System.out.println(a+a++);
	System.out.println(a);
	System.out.println(a+a++);
	System.out.println(a);
	System.out.println(a+a++);
	System.out.println(a);
	System.out.println(a+a++);
	System.out.println(a+(--a));
	System.out.println(a+(--a));
	System.out.println(a+(--a));
	System.out.println(a+(++a));
	System.out.println(a+(++a));
	System.out.println(a+(a--));
	System.out.println(a+(a--));
}

}

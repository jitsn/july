package operaters;

public class Operators4 
{public static void main(String[] args)
{double a=1234567,d=1234567;


//pre & post increment
System.out.println("pre & post increment:");
System.out.println("the value of a is:"+(a));
System.out.println("the value of pre increament is:"+(++a));
System.out.println("the value of d is:"+(d));
System.out.println("the value of post increament is:"+(d++));
System.out.println("the value of d is:"+(d));

//pre & post Decrement
System.out.println("pre & post Decrement:");
System.out.println("the value of a is:"+(a));
System.out.println("the value of pre Decrement is:"+(--a));
System.out.println("the value of d is:"+(d));
System.out.println("the value of post Decrement is:"+(d--));
System.out.println("the value of d is:"+(d));
}

}
